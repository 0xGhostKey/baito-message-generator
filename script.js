//     直前と異なるパターンをランダムで再生成する
// ============================================================
document.getElementById('variantBtn').addEventListener('click', function() {
  showResult(generateMessage());
});


// ============================================================
//  10. 「コピーする」ボタンのクリック処理
// ============================================================
document.getElementById('copyBtn').addEventListener('click', function() {
  // textarea に変更したので value で現在の内容を取得（ユーザーが編集した内容も含む）
  const text = document.getElementById('resultText').value;

  // クリップボードにコピー（navigator.clipboard は https または localhost で使用可能）
  navigator.clipboard.writeText(text).then(function() {
    showCopySuccess();
  }).catch(function() {
    // クリップボードAPIが使えない環境（http など）向けのフォールバック
    // textarea の場合は select() が使えるのでシンプルに書ける
    const textarea = document.getElementById('resultText');
    textarea.select();
    document.execCommand('copy');
    showCopySuccess();
  });
});

// コピー完了メッセージを2秒間表示する関数
function showCopySuccess() {
  const successMsg = document.getElementById('copySuccess');
  successMsg.classList.remove('hidden');
  setTimeout(function() {
    successMsg.classList.add('hidden');
  }, 2000);
}


// ============================================================
//  11. 「よく使う例」カードのクリック処理
//      クリックすると対応する選択ボタンを自動でセットする
// ============================================================
document.querySelectorAll('.quick-card').forEach(function(card) {
  card.addEventListener('click', function() {
    // カードの data 属性から設定値を読み取る
    selected.purpose  = card.dataset.purpose;
    selected.relation = card.dataset.relation;
    selected.polite   = card.dataset.polite;
    selected.length   = card.dataset.length;

    // 補足テキストをセット（data-note が指定されていれば）
    document.getElementById('note').value = card.dataset.note || '';

    // 各ボタングループの見た目（active クラス）を更新する
    ['purpose', 'relation', 'polite', 'length'].forEach(function(group) {
      document.querySelectorAll('.select-btn[data-group="' + group + '"]').forEach(function(btn) {
        // 選択値と一致するボタンだけ active にする
        btn.classList.toggle('active', btn.dataset.value === selected[group]);
      });
    });

    // フォームカードまでなめらかにスクロール
    document.getElementById('formCard').scrollIntoView({ behavior: 'smooth', block: 'start' });
  });
});
