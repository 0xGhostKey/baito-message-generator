// ============================================================
//  4. 補足テキストをフォーマットする関数
//     入力がある場合のみ（補足：〇〇）という形式で返す
// ============================================================
function formatNote(noteText) {
  const trimmed = noteText.trim();
  if (!trimmed) {
    return '';   // 入力がなければ空文字を返す
  }
  // 改行を前後に入れてメッセージに自然に組み込む
  return '\n（' + trimmed + '）\n';
}


// ============================================================
//  5. パターン配列からランダムに1つ選ぶ関数
//     直前と同じインデックスが連続しないようにする
// ============================================================
function pickPattern(patterns) {
  // パターンが1つしかない場合はそのまま返す
  if (patterns.length === 1) {
    lastPatternIndex = 0;
    return patterns[0];
  }

  // 前回と異なるインデックスをランダムで選ぶ
  let index;
  do {
    index = Math.floor(Math.random() * patterns.length);
  } while (index === lastPatternIndex);

  // 今回のインデックスを記録しておく
  lastPatternIndex = index;
  return patterns[index];
}


// ============================================================
//  6. メッセージを生成する関数
// ============================================================
function generateMessage() {
  const noteInput = document.getElementById('note').value;

  // 挨拶文と本文テンプレート配列を取得
  const greeting = greetings[selected.relation][selected.polite];
  const patterns = templates[selected.purpose][selected.polite][selected.length];

  // パターン配列からランダムに1つ選ぶ
  const bodyTemplate = pickPattern(patterns);

  // 補足テキストを整形して {note} を置き換える
  const noteText = formatNote(noteInput);
  const body = bodyTemplate.replace('{note}', noteText);

  // 挨拶 + 本文を改行でつなぐ
  return greeting + '\n' + body;
}


// ============================================================
//  7. 結果エリアを表示してメッセージをセットするヘルパー
// ============================================================
function showResult(message) {
  const resultText = document.getElementById('resultText');
  const resultArea = document.getElementById('resultArea');

  resultText.value = message;   // textarea なので value を使う
  resultArea.classList.remove('hidden');
  resultArea.scrollIntoView({ behavior: 'smooth', block: 'start' });
}


// ============================================================
//  8. 「メッセージを生成する」ボタンのクリック処理
//     生成するたびに lastPatternIndex をリセットして新鮮なランダムにする
// ============================================================
document.getElementById('generateBtn').addEventListener('click', function() {
  lastPatternIndex = -1;   // リセットして偏りをなくす
  showResult(generateMessage());
});


// ============================================================
