// ============================================================
//  2. ボタンのクリックで選択状態を切り替える処理
//     data-group が一致するボタンのグループを切り替える
// ============================================================
document.querySelectorAll('.select-btn').forEach(function(btn) {
  btn.addEventListener('click', function() {
    const group = btn.dataset.group;   // どのグループのボタンか
    const value = btn.dataset.value;   // 選ばれた値

    // 同じグループの全ボタンから 'active' クラスを外す
    document.querySelectorAll('.select-btn[data-group="' + group + '"]').forEach(function(b) {
      b.classList.remove('active');
    });

    // クリックしたボタンに 'active' クラスを付ける
    btn.classList.add('active');

    // 選択状態オブジェクトを更新
    selected[group] = value;
  });
});


